package org.finance.bank.model;

/**
 *
 * @author oscar
 */
public class BibliotecaException extends Exception {

    private static final long serialVersionUID = 6088926774212431719L;

    public BibliotecaException() {
    }

    public BibliotecaException(String e) {
        super(e);
    }

    public BibliotecaException(Throwable e) {
        super(e);
        e.printStackTrace();
    }

    public BibliotecaException(String s, Throwable e) {
        super(s, e);
        e.printStackTrace();
    }
}
